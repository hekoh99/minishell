#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*nxt;

	if (!lst || !f)
		return ;
	nxt = lst;
	while (nxt)
	{
		f(nxt->content);
		nxt = nxt->next;
	}
}
