#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		size;
	t_list	*nxt;

	size = 0;
	nxt = lst;
	while (nxt)
	{
		nxt = nxt->next;
		size++;
	}
	return (size);
}
